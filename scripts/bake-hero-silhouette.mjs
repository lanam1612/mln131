import { geoMercator, geoPath } from 'd3-geo'
import fs from 'node:fs'

// Regenerate src/lib/vietnamSilhouette.ts from the canonical GeoJSON used by
// the Phase 4 3D map. Run: `node scripts/bake-hero-silhouette.mjs`.

const SRC = 'public/assets/models/vietnam.geojson'
const OUT = 'src/lib/vietnamSilhouette.ts'

const fc = JSON.parse(fs.readFileSync(SRC, 'utf8'))

// Decimate the largest polygon 8:1 so the baked path stays around 16KB.
const main = JSON.parse(JSON.stringify(fc.features[0]))
const poly0 = main.geometry.coordinates[0][0]
main.geometry.coordinates[0][0] = poly0.filter(
  (_, i) => i % 8 === 0 || i === poly0.length - 1,
)

const proj = geoMercator().fitExtent([[15, 15], [275, 425]], fc)
const path = geoPath(proj)
const d = path(main)

const project = (lon, lat) => {
  const [x, y] = proj([lon, lat])
  return [Number(x.toFixed(2)), Number(y.toFixed(2))]
}

const dots = {
  taybac: project(104, 22),
  namtrungbo: project(109.1, 13.4),
  taynguyen: project(108, 13),
  taynambo: project(105.5, 10),
}
const islands = {
  hoangSa: project(112, 16.5),
  truongSa: project(114, 9),
}

const body = `// Auto-generated by scripts/bake-hero-silhouette.mjs from
// public/assets/models/vietnam.geojson — do not hand-edit. Projection:
// d3-geo Mercator, fitExtent [[15,15],[275,425]] in a 290x440 viewBox.
// Largest polygon decimated 8:1 (~440 vertices).

// SVG path of Vietnam mainland + coastal islands (Phú Quốc, Côn Đảo, …).
// prettier-ignore
export const VIETNAM_OUTLINE_PATH =
  ${JSON.stringify(d)}

// Projected centroids in SVG units, aligned with VIETNAM_OUTLINE_PATH.
export const REGION_PROJECTED: Record<
  'taybac' | 'taynguyen' | 'taynambo' | 'namtrungbo',
  { cx: number; cy: number }
> = {
  taybac: { cx: ${dots.taybac[0]}, cy: ${dots.taybac[1]} },
  namtrungbo: { cx: ${dots.namtrungbo[0]}, cy: ${dots.namtrungbo[1]} },
  taynguyen: { cx: ${dots.taynguyen[0]}, cy: ${dots.taynguyen[1]} },
  taynambo: { cx: ${dots.taynambo[0]}, cy: ${dots.taynambo[1]} },
}

export const ISLAND_PROJECTED = {
  hoangSa: { cx: ${islands.hoangSa[0]}, cy: ${islands.hoangSa[1]} },
  truongSa: { cx: ${islands.truongSa[0]}, cy: ${islands.truongSa[1]} },
}
`

fs.writeFileSync(OUT, body)
console.log(`wrote ${OUT}: ${body.length} bytes, path ${d.length} bytes`)
